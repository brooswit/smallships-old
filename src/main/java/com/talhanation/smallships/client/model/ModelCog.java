    @Override
    public void setupAnim(T entityIn, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch) {
        int state = entityIn.getSailState();
        switch (state){
            case 0:

                this.Sail_z0.visible= true;
                this.Sail_z1.visible= false;
                this.Sail_z2.visible= false;
                this.Sail_z3.visible= false;
                this.Sail_z4.visible= false;
                break;

            case 1:
                this.Sail_z0.visible= false;
                this.Sail_z1.visible= true;
                this.Sail_z2.visible= false;
                this.Sail_z3.visible= false;
                this.Sail_z4.visible= false;
                break;

            case 2:
                this.Sail_z0.visible= false;
                this.Sail_z1.visible= false;
                this.Sail_z2.visible= true;
                this.Sail_z3.visible= false;
                this.Sail_z4.visible= false;
                break;

            case 3:
                this.Sail_z0.visible= false;
                this.Sail_z1.visible= false;
                this.Sail_z2.visible= false;
                this.Sail_z3.visible= true;
                this.Sail_z4.visible= false;
                break;

            case 4:
                this.Sail_z0.visible= false;
                this.Sail_z1.visible= false;
                this.Sail_z2.visible= false;
                this.Sail_z3.visible= false;
                this.Sail_z4.visible= true;
                break;
        }

        this.Cargo0.visible = (entityIn).Cargo_0;
        this.Cargo1.visible = (entityIn).Cargo_1;
        this.Cargo2.visible = (entityIn).Cargo_2;
        this.Cargo3.visible = (entityIn).Cargo_3;

        if (entityIn.getSteerState(0)){
            this.steer.yRot = Mth.cos(3.1415927F);
            //this.Brigg.rotateAngleZ = -Mth.cos(3.1415927F * 2 );
        }
        else if (entityIn.getSteerState(1)) {
            this.steer.yRot = -Mth.cos(3.1415927F);
            //this.Brigg.rotateAngleZ = Mth.cos(3.1415927F * 2 );
        }
        else {
            this.steer.yRot = 0;
            //this.Brigg.rotateAngleZ = 0;
        }


    }
